package nl.iamlinda.catbook.model;

public class NextChapter {
    private int nextChapterTextId;
    private int nextPage;

    public int getNextChapterTextId() {
        return nextChapterTextId;
    }

    public void setNextChapterTextId(int nextChapterTextId) {
        this.nextChapterTextId = nextChapterTextId;
    }

    public int getNextPage() {
        return nextPage;
    }

    public void setNextPage(int nextPage) {
        this.nextPage = nextPage;
    }
}
